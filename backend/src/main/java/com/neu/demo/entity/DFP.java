package com.neu.demo.entity;

public class DFP {
    int dfp_id;
    String module_id;
    String type;
    String detail;

    public DFP() {
    }

    public int getDfp_id() {
        return dfp_id;
    }

    public void setDfp_id(int dfp_id) {
        this.dfp_id = dfp_id;
    }

    public String getModule_id() {
        return module_id;
    }

    public void setModule_id(String module_id) {
        this.module_id = module_id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }
}
